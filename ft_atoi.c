{
	int	i;
	int	n;
	int	neg;

	i = -1;
	neg = 1;
	n = 0;
	while (str[i] == ' ' || str[i] == '\t' || str[i] == '\n'
		|| str[i] == '\v' || str[i] == '\f' || str[i] == '\r')
		str++;
	while (str[++i] != '\0')
	{
		if (str[i] == '-')
			neg = neg * -1;
		else if (str[i] >= '0' && str[i] <= '9')
			n = n * 10 + (str[i] - '0');
	}
	return (n * neg);
}
